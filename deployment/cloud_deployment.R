#' Generate Kubernetes deployment configuration
#'
#' @param config Cloud deployment configuration
#' @return Kubernetes YAML configuration
generate_kubernetes_config <- function(config = CLOUD_CONFIG) {

  k8s_config <- list(
    apiVersion = "v1",
    kind = "Namespace",
    metadata = list(
      name = config$app_name,
      labels = list(
        app = config$app_name,
        environment = config$environment
      )
    )
  )

  # Deployment
  deployment <- list(
    apiVersion = "apps/v1",
    kind = "Deployment",
    metadata = list(
      name = paste0(config$app_name, "-deployment"),
      namespace = config$app_name,
      labels = list(
        app = config$app_name,
        component = "api"
      )
    ),
    spec = list(
      replicas = config$desired_instances,
      selector = list(
        matchLabels = list(
          app = config$app_name,
          component = "api"
        )
      ),
      template = list(
        metadata = list(
          labels = list(
            app = config$app_name,
            component = "api"
          )
        ),
        spec = list(
          containers = list(
            list(
              name = config$app_name,
              image = paste0(config$app_name, ":latest"),
              ports = list(
                list(containerPort = 8000, name = "api"),
                list(containerPort = 3838, name = "shiny")
              ),
              env = list(
                list(name = "ENVIRONMENT", value = config$environment),
                list(name = "DATABASE_URL", valueFrom = list(secretKeyRef = list(
                  name = paste0(config$app_name, "-secrets"),
                  key = "database_url"
                ))),
                list(name = "AWS_ACCESS_KEY_ID", valueFrom = list(secretKeyRef = list(
                  name = paste0(config$app_name, "-secrets"),
                  key = "aws_access_key_id"
                ))),
                list(name = "AWS_SECRET_ACCESS_KEY", valueFrom = list(secretKeyRef = list(
                  name = paste0(config$app_name, "-secrets"),
                  key = "aws_secret_access_key"
                )))
              ),
              resources = list(
                requests = list(
                  memory = "256Mi",
                  cpu = "250m"
                ),
                limits = list(
                  memory = "1Gi",
                  cpu = "1000m"
                )
              ),
              livenessProbe = list(
                httpGet = list(
                  path = "/health",
                  port = 8000
                ),
                initialDelaySeconds = 60,
                periodSeconds = 30,
                timeoutSeconds = 5,
                failureThreshold = 3
              ),
              readinessProbe = list(
                httpGet = list(
                  path = "/health",
                  port = 8000
                ),
                initialDelaySeconds = 30,
                periodSeconds = 10,
                timeoutSeconds = 3,
                failureThreshold = 3
              )
            )
          )
        )
      )
    )
  )

  # Service
  service <- list(
    apiVersion = "v1",
    kind = "Service",
    metadata = list(
      name = paste0(config$app_name, "-service"),
      namespace = config$app_name,
      labels = list(
        app = config$app_name,
        component = "api"
      )
    ),
    spec = list(
      selector = list(
        app = config$app_name,
        component = "api"
      ),
      ports = list(
        list(port = 80, targetPort = 8000, name = "api"),
        list(port = 3838, targetPort = 3838, name = "shiny")
      ),
      type = "ClusterIP"
    )
  )

  # Ingress
  ingress <- list(
    apiVersion = "networking.k8s.io/v1",
    kind = "Ingress",
    metadata = list(
      name = paste0(config$app_name, "-ingress"),
      namespace = config$app_name,
      annotations = list(
        "kubernetes.io/ingress.class" = "nginx",
        "cert-manager.io/cluster-issuer" = "letsencrypt-prod",
        "nginx.ingress.kubernetes.io/rewrite-target" = "/$2"
      )
    ),
    spec = list(
      tls = list(
        list(
          hosts = list(config$domain),
          secretName = paste0(config$app_name, "-tls")
        )
      ),
      rules = list(
        list(
          host = config$domain,
          http = list(
            paths = list(
              list(
                path = "/()(.*)",
                pathType = "Prefix",
                backend = list(
                  service = list(
                    name = paste0(config$app_name, "-service"),
                    port = list(number = 80)
                  )
                )
              )
            )
          )
        )
      )
    )
  )

  # ConfigMap for application settings
  configmap <- list(
    apiVersion = "v1",
    kind = "ConfigMap",
    metadata = list(
      name = paste0(config$app_name, "-config"),
      namespace = config$app_name
    ),
    data = list(
      ENVIRONMENT = config$environment,
      LOG_LEVEL = "INFO",
      MAX_WORKERS = as.character(config$parallel_cores),
      CACHE_ENABLED = "TRUE",
      BACKUP_ENABLED = "TRUE"
    )
  )

  # Secret for sensitive data
  secret <- list(
    apiVersion = "v1",
    kind = "Secret",
    metadata = list(
      name = paste0(config$app_name, "-secrets"),
      namespace = config$app_name
    ),
    type = "Opaque",
    data = list(
      database_url = openssl::base64_encode(paste0("postgresql://admin:", random_password.db_password.result, "@", aws_db_instance.main.endpoint, "/nhanes_analysis")),
      aws_access_key_id = openssl::base64_encode("your_access_key"),
      aws_secret_access_key = openssl::base64_encode("your_secret_key")
    )
  )

  return(list(
    namespace = k8s_config,
    deployment = deployment,
    service = service,
    ingress = ingress,
    configmap = configmap,
    secret = secret
  ))
}

